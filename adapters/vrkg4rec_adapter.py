# ---------------------------------------------------------------------------
# Lookup helpers
# ---------------------------------------------------------------------------

def load_lookups(dataset: str) -> dict:
    """Load lookup tables produced by vrkg4rec_data_prep.py."""
    lookup_dir = Path(__file__).parent / 'vrkg_lookups' / dataset
    if not lookup_dir.exists():
        raise FileNotFoundError(
            f"Lookup directory not found: {lookup_dir}\n"
            f"Run adapters/vrkg4rec_data_prep.py first."
        )
    with open(lookup_dir / 'entity_to_xrecsys.json') as f:
        raw = json.load(f)
    entity_to_xrecsys = {int(k): v for k, v in raw.items()}

    with open(lookup_dir / 'uid_remap.json') as f:
        raw = json.load(f)
    vrkg_to_xrecsys_uid = {int(k): v for k, v in raw.items()}

    with open(lookup_dir / 'meta.json') as f:
        meta = json.load(f)

    return {
        'entity_to_xrecsys':   entity_to_xrecsys,
        'vrkg_to_xrecsys_uid': vrkg_to_xrecsys_uid,
        'n_items':             meta['n_items'],
    }


def load_xrecsys_kg(xrecsys_dir: Path, dataset: str):
    """Load kg.pkl for relation lookup."""
    sys.path.insert(0, str(xrecsys_dir / 'models' / 'PGPR'))
    kg_path = xrecsys_dir / 'models' / 'PGPR' / 'tmp' / dataset / 'kg.pkl'
    with open(kg_path, 'rb') as f:
        return pickle.load(f)


def find_relation(G, kg_relation_map, src_etype, src_lid, dst_etype, dst_lid):
    """
    Return the relation name connecting (src_etype, src_lid) → (dst_etype, dst_lid).
    Returns None if edge not found.
    """
    if src_etype not in G or src_lid not in G[src_etype]:
        return None
    neighbors = G[src_etype][src_lid]
    rel_dict  = kg_relation_map.get(src_etype, {})
    for rel_name, expected_dst_etype in rel_dict.items():
        if expected_dst_etype != dst_etype:
            continue
        if rel_name not in neighbors:
            continue
        if dst_lid in neighbors[rel_name]:
            return rel_name
    return None


# ---------------------------------------------------------------------------
# Path conversion
# ---------------------------------------------------------------------------

def convert_path(path_nodes, vrkg_uid, xrecsys_uid, dataset, lookups, kg_G):
    """
    Convert a VRKG4Rec integer-node path to an xrecsys path string.

    4-node path → 12-token xrecsys string (full LIR/SEP/ETD support)
    3-node path →  9-token xrecsys string (LIR = 0, SEP/ETD supported)
    Other lengths → None (skipped)
    """
    n = len(path_nodes)
    if n < 3 or n > 4:
        return None

    item_etype  = ITEM_ETYPE[dataset]
    user_rel    = USER_RELATION[dataset]
    entity_map  = lookups['entity_to_xrecsys']
    n_items     = lookups['n_items']
    kg_rel_map  = KG_RELATION[dataset]

    # Resolve item (last node): entity_id == item_index == xrecsys local_id
    item_global = path_nodes[-1]
    if item_global >= n_items:
        return None
    pid = item_global

    if n == 4:
        # Full path: uid → e1(seed item) → e2(bridge entity) → item
        e1 = entity_map.get(path_nodes[1])
        e2 = entity_map.get(path_nodes[2])
        if e1 is None or e2 is None:
            return None

        e1_etype, e1_lid = e1['etype'], e1['local_id']
        e2_etype, e2_lid = e2['etype'], e2['local_id']

        # e1 must be an item for LIR to work
        if e1_etype != item_etype:
            return None

        rel2 = find_relation(kg_G.G, kg_rel_map, e1_etype, e1_lid, e2_etype, e2_lid)
        rel3 = find_relation(kg_G.G, kg_rel_map, e2_etype, e2_lid, item_etype, pid)
        if rel2 is None or rel3 is None:
            return None

        tokens = [
            'self_loop', 'user',      str(xrecsys_uid),
            user_rel,     item_etype, str(e1_lid),
            rel2,         e2_etype,   str(e2_lid),
            rel3,         item_etype, str(pid),
        ]

    else:  # n == 3: uid → e1(seed item) → item  (LIR = 0)
        e1 = entity_map.get(path_nodes[1])
        if e1 is None:
            return None
        e1_etype, e1_lid = e1['etype'], e1['local_id']
        if e1_etype != item_etype:
            return None

        rel2 = find_relation(kg_G.G, kg_rel_map, e1_etype, e1_lid, item_etype, pid)
        if rel2 is None:
            return None

        # 9-token path (3 triples) — normalize_path handles variable length
        tokens = [
            'self_loop', 'user',      str(xrecsys_uid),
            user_rel,     item_etype, str(e1_lid),
            rel2,         item_etype, str(pid),
        ]

    return ' '.join(tokens)


# ---------------------------------------------------------------------------
# Main conversion
# ---------------------------------------------------------------------------

def convert(
    pkl_path: str,
    dataset: str,
    xrecsys_dir: str,
    topk: int = 10,
    agent_topk_tag: str = None,
) -> None:
    pkl_path    = Path(pkl_path)
    xrecsys_dir = Path(xrecsys_dir)

    print(f"Loading paths pkl: {pkl_path}")
    with open(pkl_path, 'rb') as f:
        data = pickle.load(f)
    print(f"  {len(data)} users in pkl")

    print("Loading lookup tables ...")
    lookups = load_lookups(dataset)
    print(f"  entities: {len(lookups['entity_to_xrecsys']):,}  users: {len(lookups['vrkg_to_xrecsys_uid']):,}")

    print("Loading xrecsys KG for relation lookup ...")
    kg_G = load_xrecsys_kg(xrecsys_dir, dataset)

    print("Loading train labels ...")
    train_set = load_train_labels(xrecsys_dir, dataset)

    vrkg_to_xrecsys_uid = lookups['vrkg_to_xrecsys_uid']

    # ------------------------------------------------------------------
    # Score normalisation
    # ------------------------------------------------------------------
    score_list = []
    for vrkg_uid, pid_dict in data.items():
        xuid = vrkg_to_xrecsys_uid.get(vrkg_uid)
        if xuid is None:
            continue
        for item_idx, paths in pid_dict.items():
            if xuid in train_set and item_idx in train_set[xuid]:
                continue
            for _, score in paths:
                score_list.append(float(score))

    if not score_list:
        raise ValueError("No valid non-train paths found in pkl.")

    min_s = min(score_list)
    max_s = max(score_list)
    rng   = max_s - min_s if max_s != min_s else 1.0
    print(f"  score range: [{min_s:.4f}, {max_s:.4f}]")

    # ------------------------------------------------------------------
    # Build output structures
    # ------------------------------------------------------------------
    pred_rows        = []
    uid_topk         = {}
    uid_pid_best     = {}
    skipped_no_uid   = 0
    skipped_convert  = 0

    for vrkg_uid, pid_dict in data.items():
        xuid = vrkg_to_xrecsys_uid.get(vrkg_uid)
        if xuid is None:
            skipped_no_uid += 1
            continue

        items_scored = []
        for item_idx, paths in pid_dict.items():
            pid = item_idx          # item_index == xrecsys song local_id
            if xuid in train_set and pid in train_set[xuid]:
                continue

            best_score = float('-inf')
            best_path  = None

            for path_nodes, score in paths:
                score = float(score)
                norm  = (score - min_s) / rng
                path_str = convert_path(
                    path_nodes, vrkg_uid, xuid, dataset, lookups, kg_G)

                if path_str:
                    pred_rows.append((xuid, pid, norm, -1, path_str))
                    if score > best_score:
                        best_score = score
                        best_path  = path_str
                else:
                    skipped_convert += 1
                    if score > best_score:
                        best_score = score

            if best_score > float('-inf'):
                items_scored.append((pid, best_score, best_path))

        items_scored.sort(key=lambda x: x[1], reverse=True)
        top_items          = items_scored[:topk]
        uid_topk[xuid]     = [p for p, _, _ in top_items]
        uid_pid_best[xuid] = {p: ps for p, _, ps in top_items if ps is not None}

    print(f"  skipped (no uid map)  : {skipped_no_uid}")
    print(f"  skipped (bad format)  : {skipped_convert:,}")
    print(f"  pred_rows             : {len(pred_rows):,}")

    # ------------------------------------------------------------------
    # Write CSVs
    # ------------------------------------------------------------------
    if agent_topk_tag is None:
        agent_topk_tag = f"{topk}-{topk + 2}-1"
    output_dir = xrecsys_dir / 'paths' / dataset / f'agent_topk={agent_topk_tag}'

    print(f"Writing CSVs to {output_dir} ...")
    write_csvs(output_dir, pred_rows, uid_topk, uid_pid_best)

    print("Done.")
    print(f"  pred_paths.csv          : {len(pred_rows):,} rows")
    print(f"  uid_topk.csv            : {len(uid_topk):,} users")
    print(f"  uid_pid_explanation.csv : {sum(len(v) for v in uid_pid_best.values()):,} uid-pid pairs")


if __name__ == '__main__':
    parser = argparse.ArgumentParser(
        description='Convert VRKG4Rec paths pkl to xrecsys CSVs')
    parser.add_argument('--pkl',            required=True,
                        help='Path to VRKG4Rec cached paths pkl')
    parser.add_argument('--dataset',        required=True,
                        choices=['lastfm', 'ml1m'],
                        help='xrecsys dataset name')
    parser.add_argument('--xrecsys-dir',    default='xrecsys',
                        help='Root of xrecsys clone (default: xrecsys/)')
    parser.add_argument('--topk',           type=int, default=10,
                        help='Top-K items per user (default: 10)')
    parser.add_argument('--agent-topk-tag', default=None,
                        help='Folder tag e.g. 10-12-1; inferred if omitted')
    args = parser.parse_args()

    convert(
        pkl_path=args.pkl,
        dataset=args.dataset,
        xrecsys_dir=args.xrecsys_dir,
        topk=args.topk,
        agent_topk_tag=args.agent_topk_tag,
    )
