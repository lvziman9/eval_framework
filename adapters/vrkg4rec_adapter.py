"""
VRKG4Rec Adapter
================
Converts VRKG4Rec output paths.pkl into the three CSV files expected by
xrecsys evaluator, using lookup tables generated by vrkg4rec_data_prep.py.

Path format in VRKG4Rec pkl
----------------------------
  {vrkg_uid (int): {item_index (int): [(path_nodes, score), ...]}}

  - vrkg_uid    : 0-indexed user ID (as assigned by data_prep)
  - item_index  : 0-indexed item ID (= xrecsys song/movie local_id in our scheme)
  - path_nodes  : list of integer node IDs
                    path_nodes[0]  = vrkg_uid  (user space)
                    path_nodes[1:] = entity IDs (entity space, 0-indexed)
                    path_nodes[-1] = entity_id of item (= item_index in our scheme)
  - score       : float, higher = better path

xrecsys path string format (12 tokens = 4 triples)
----------------------------------------------------
  "self_loop user {xuid}  {user_rel} {item_etype} {seed_id}  {rel} {etype} {bridge_id}  {rel} {item_etype} {pid}"

  path[0] = (self_loop, user, xuid)                     — always
  path[1] = (listened/watched, song/movie, seed_id)     — LIR anchor: training item
  path[2] = (rel, bridge_etype, bridge_id)              — SEP anchor: bridging entity
  path[3] = (rel, song/movie, pid)                      — ETD anchor: target item

3-node VRKG4Rec paths [uid, e1, item] produce 9-token xrecsys paths (LIR = 0).

Pre-requisite
-------------
  Run adapters/vrkg4rec_data_prep.py once before training VRKG4Rec to generate
  the lookup files in adapters/vrkg_lookups/<dataset>/.

Output (written to xrecsys/paths/<dataset>/agent_topk=<tag>/)
    pred_paths.csv           uid, pid, path_score, path_prob, path
    uid_topk.csv             uid, top10
    uid_pid_explanation.csv  uid, pid, path

Usage (from eval_framework/ root):
    python adapters/vrkg4rec_adapter.py \\
        --pkl  /path/to/vrkg4rec/interpretability_results/cached_paths_*.pkl \\
        --dataset lastfm \\
        --xrecsys-dir xrecsys \\
        --topk 10 \\
        --agent-topk-tag 10-12-1
"""

